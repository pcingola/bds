package org.bds.lang.nativeMethods.string;

import org.bds.data.Data;
import org.bds.lang.Parameters;
import org.bds.lang.type.Type;
import org.bds.lang.type.TypeList;
import org.bds.lang.type.Types;
import org.bds.lang.value.Value;
import org.bds.lang.value.ValueList;
import org.bds.lang.value.ValueString;
import org.bds.run.BdsThread;

public class MethodNativeStringDir extends MethodNativeString {

	private static final long serialVersionUID = -8013491174060654835L;

	public MethodNativeStringDir() {
		super();
	}

	@Override
	protected void initMethod() {
		functionName = "dir";
		classType = Types.STRING;
		returnType = TypeList.get(Types.STRING);

		String[] argNames = { "this" };
		Type[] argTypes = { Types.STRING };
		parameters = Parameters.get(argTypes, argNames);
		addNativeMethodToClassScope();
	}

	@Override
	public Value runMethod(BdsThread bdsThread, Value vthis) {
		Data dir = bdsThread.data(vthis.asString());

		ValueList vlist = new ValueList(returnType);
		for (Data d : dir.list())
			vlist.add(new ValueString(d.getName()));

		vlist.sort();
		return vlist;
	}

	@Override
	protected Object runMethodNative(BdsThread bdsThread, Object objThis) {
		bdsThread.runtimeError("This method should never be invoked!");
		return null;
	}

}
